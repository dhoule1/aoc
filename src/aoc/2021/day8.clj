(defn part1 [input]
  (->> input
       (str/split-lines)
       (map (fn [line] (second (first (re-seq #"\| ([a-g ]+)" line)))))
       (mapcat #(re-seq #"[a-g]+" %))
       (filter #(contains? #{2 3 4 7} (count %)))
       count))

